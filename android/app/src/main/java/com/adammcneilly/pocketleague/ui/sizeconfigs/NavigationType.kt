package com.adammcneilly.pocketleague.ui.sizeconfigs

/**
 * The [NavigationType] defines how a user can navigate within screens at the base level
 * of the application.
 *
 * @property[BOTTOM_NAVIGATION] When selected, the user will see navigation tabs at
 * the bottom of their screen.
 * @property[NAVIGATION_RAIL] When selected, the user will see navigation tabs on the side of
 * their screen.
 */
enum class NavigationType {
    BOTTOM_NAVIGATION,
    NAVIGATION_RAIL,
}
